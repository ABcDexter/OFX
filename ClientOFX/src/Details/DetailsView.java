    public void updateDetails(Item item) {
        jTextField1.setText(item.itemname);
        jTextField6.setText(item.price + "");
        jTextField3.setText(Hardcoding.getCategory(item.category));
        jTextField4.setText(Hardcoding.getSubcategory(item.category, item.subcategory));
        jTextField2.setText(item.brand);
        jSpinner1.setValue(item.quantity);
        jTextArea1.setText(item.description);
        jTextField5.setText(item.location);
        jTextField8.setText(item.contact);
    }
//------------------------------------------------------------------------------

    public void updateSellerDetails(User user) {
        jTextField7.setText(user.firstname + " " + user.lastname);
        jTextField9.setText(Hardcoding.getHostelName(user.hostel) + " " + user.room);
    }
//------------------------------------------------------------------------------

    public boolean showConfirmation() {
        return JOptionPane.showConfirmDialog(null, "Are you sure you want to buy "
                + "this item?", "Confirm Buy", JOptionPane.YES_NO_CANCEL_OPTION)
                == JOptionPane.YES_OPTION;
    }
//------------------------------------------------------------------------------

    public void dealDone() {
        JOptionPane.showMessageDialog(null, "This item has been successfully purchased by you!");
    }
//------------------------------------------------------------------------------

    public void dealFailure() {
        JOptionPane.showMessageDialog(null, "Transaction failed: item sold out\n"
                + "This might have happened because someone bought the item \n"
                + "before you decided to buy");
    }
